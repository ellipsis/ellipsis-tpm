load utils

##############################################################################

msg.print() {
    if [ -n "$TPM_TMUX_ECHO" ]; then
        tmux.echo "$(msg.log "$@")"
    elif [ -t 1 ]; then
        echo -e "$@"
    else
        msg.log "$@"
    fi
}

##############################################################################

msg.log() {
    utils.strip_colors "$(echo -e "$@")"
}

##############################################################################

msg.bold() {
    msg.print "\033[1m$@\033[0m"
}

##############################################################################

msg.dim() {
    msg.print "\033[90m$@\033[0m"
}

